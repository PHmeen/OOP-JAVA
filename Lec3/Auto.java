public class Auto {
    public static void main(String[] args) {
        float x = 100;

        double f = 10.0;
        f = f * 3.1 + f / 2;

        byte b = 50;
        b = (byte)(b * 2);

        System.out.println("x = " + x);
        System.out.println("f = " + f);
        System.out.println("b = " + b);
    }
}
