class Lab4_6 {
    public static void main(String[] args) {
        double x = 5.5, y = 2.0;
        int m;
        m = (int) x / (int) y;
        System.out.println("m =" + m);
    }
}
// Cast Operator นี้ ไม่ได้ไปยุ่งกับตัวแปร x และ y
// ตัวแปร x มีค่า 5.5 เหมือนเดิม
// ตัวแปร y มีค่า 2.0 เหมือนเดิม